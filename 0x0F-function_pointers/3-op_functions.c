#include <stdio.h>
#include <stdlib.h>

/**
  *op_add- function to add a and b
  *@a: first value
  *@b: second value
  *Return: sum of and b
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  *op_sub- function to subtract a and b
  *@a: first value
  *@b: second value
  *Return: difference of a and b
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  *op_mul- function to multiply a and b
  *@a: first value
  *@b: second value
  *Return: productof a and b
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  *op_div- function to divide a and b
  *@a: first value
  *@b: second value
  *Return: a / b
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  *op_mod- function to find modulus a and b
  *@a: first value
  *@b: second value
  *Return: modulus of a and b
  */

int op_mod(int a, int b)
{
	return (a % b);
}
